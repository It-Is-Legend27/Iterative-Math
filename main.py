import ITMATH

# Printing the number of combinations of 4 things taken 2 at a time.
print(ITMATH.combinations(4,2))

# Taking the exponential of 2.
print(ITMATH.exp(2))

# Taking the power of 2 to the power of 3.
print(ITMATH.pow(2,3))

# Takes the factorial of 4
print(ITMATH.factorial(4))

# Permuation of 4 items taken 2 at a time
print(ITMATH.permutations(4,2))

# Calculates summation using closed form
print(ITMATH.summation(2,1,4))